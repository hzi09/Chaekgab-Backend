def save_to_db(all_books):
    conn = psycopg2.connect(
        host=DB_HOST,
        port=DB_PORT,
        dbname=DB_NAME,
        user=DB_USER,
        password=DB_PASSWORD
    )
    cur = conn.cursor()
    # 테이블이 없으면 생성
    cur.execute('''
        CREATE TABLE IF NOT EXISTS books (
            id SERIAL PRIMARY KEY,
            category VARCHAR(100),
            title VARCHAR(100),
            author VARCHAR(200),
            publisher VARCHAR(200),
            bookcover TEXT,
            created_at TIMESTAMP DEFAULT now(),
            updated_at TIMESTAMP DEFAULT now()
        )
    ''')
    for book in all_books:
        cur.execute(
            """INSERT INTO books (category, title, author, publisher, bookcover, created_at, updated_at)
            VALUES (%s, %s, %s, %s, %s, now(), now())""",
            (book['카테고리'], book['제목'], book['지은이'], book['출판사'], book['표지이미지'])
        )
    conn.commit()
    cur.close()
    conn.close()
    print('모든 카테고리의 책 메타데이터가 DB에 저장되었습니다.')

def crawl_category(cid, category):
    books = []
    page = 0
    
    while True:
        page += 1
        url = f"https://www.aladin.co.kr/shop/wbrowse.aspx?CID={cid}&PAGE={page}"
        print(f"\n현재 페이지: {page}")
        print(f"URL: {url}")
        
        for retry in range(3):  # 최대 3번 재시도
            try:
                response = requests.get(
                    url,
                    headers={'User-Agent': 'Mozilla/5.0'},
                    timeout=10
                )
                response.raise_for_status()
                break  # 성공하면 반복문 탈출
            except Exception as e:
                print(f"요청 실패 (재시도 {retry+1}/3): {e}")
                time.sleep(2)
        else:
            print("3회 이상 실패, 다음 페이지로 넘어감")
            break
        
        soup = BeautifulSoup(response.text, 'html.parser')
        
        # 책 정보가 있는 div 찾기
        book_boxes = soup.find_all('div', {'class': 'ss_book_box'})
        print(f"찾은 책 개수: {len(book_boxes)}")
        
        if not book_boxes:
            print("책 정보를 찾을 수 없습니다. 마지막 페이지입니다.")
            break
        
        for book_box in book_boxes:
            # 표지 이미지 추출 로직 보완
            cover_area_other = book_box.find('div', class_='cover_area_other')
            bookcover = None
            if cover_area_other:
                img_tag = cover_area_other.find('img')
                if img_tag:
                    bookcover = img_tag['src']
            if not bookcover:
                img_tag = book_box.find('img', class_='front_cover')
                if img_tag:
                    bookcover = img_tag['src']
                else:
                    i_cover_img = book_box.find('img', class_='i_cover')
                    bookcover = i_cover_img['src'] if i_cover_img else None

            title = book_box.find('a', class_='bo3').get_text(strip=True)

            author = ""
            publisher = ""

            lis = book_box.find_all('li')
            for li in lis:
                # 저자(지은이) 추출
                for tag in li.find_all('a', href=re.compile(r'AuthorSearch=')):
                    role = tag.next_sibling
                    if role and '(지은이)' in role:
                        author = tag.get_text(strip=True)
                # 출판사 추출
                for tag in li.find_all('a', href=re.compile(r'PublisherSearch=')):
                    publisher = tag.get_text(strip=True)

            print(title)
            print(author)
            print(publisher)
            print(bookcover)

            # books 리스트에 추가
            books.append({
                '카테고리': category,
                '제목': title,
                '지은이': author,
                '출판사': publisher,
                '표지이미지': bookcover
            })

    return books

